            this.sidebarRight = vm.$z_sidebar.fromTemplate('<h5>右边栏</h5>', {position: 'right'});

            // setTimeout(() => {$app.setTitle('标题文字')}, 1000)

        },

        destroyed() {
            let vm = this;
            vm.$z_sidebar.destroy()
        },

        methods: {
            back() {
                let vm = this;
                vm.$router.push({path: '/'})
            },

            loading(tips) {
                let vm = this;
                vm.$z_loading.show(tips)

                setTimeout(() => {
                    vm.$z_loading.hide()
                }, 2000)
            },

            toast() {
                let vm = this;
                vm.$z_toast.show('发送成功', 1500).then(() => {
                    console.log('toast hide')
                })
            },

            toggleSidebar() {
                this.sidebar.toggle()
            },

            toggleSidebarRight() {
                this.sidebarRight.toggle()
            }
        }
    }
</script>
